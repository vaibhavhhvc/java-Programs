import java.util.*; 
class ArithmeticOperator
{ 
public static void main( String args[] ) 
{ 
int a = 10, b = 2, r ; 
// printing a and b 
System.out.println("a is " + a + " and b is " + b); 

		r = a + b; // addition 
		System.out.println("a+b is " + r); 
		
		r = a - b ;
		System.out.println("a-b is " + r); 
		
		r = a * b ;
		System.out.println("a*b is " + r); 
		
		r = a/b;
		System.out.println("a/b is " + r); 
		
		r = a%b;
		System.out.println("a%b is " + r); 

	} 
}
